import concurrent.futures
import requests
import re


def fetch_url(data):
    """
    账密，并发访问网址
    :param data:
    :return:
    """
    # 给定的字符串

    # 正则表达式匹配每个冒号前后的字符
    matches = re.findall(r'([^:]+):([^:]+)', data)

    ipdata = []

    for _ in matches:
        ipdata.append(_[0])
        ipdata.append(_[1])

    port = int(ipdata[1])
    ipdata[1] = port

    testip = ipdata[0]
    testport = ipdata[1]
    username = ipdata[2]
    password = ipdata[3]

    proxy = f'socks5://{username}:{password}@{testip}:{testport}'

    # 设置代理的地址和端口
    proxies = {
        'http': proxy,
        'https': proxy,
    }

    url = "https://ipip.922proxy.com/ip_info"

    try:
        response = requests.post(url=url, proxies=proxies, timeout=10)
    except requests.exceptions.RequestException as e:
        print(f"{e}！", flush=True)
    else:
        print(f"{response.json()}", flush=True)


